"""Node modules plugin for bekas."""

from __future__ import annotations

import os
from collections.abc import Iterator
from datetime import datetime, timedelta
from pathlib import Path

from bekas.models import Candidate, Confidence, Context, RemovalResult
from bekas.plugin import Plugin


class NodeModulesPlugin(Plugin):
    """Finds old node_modules directories."""

    name = "node.modules"
    description = "Finds node_modules directories in old or abandoned projects."
    requires_commands = []

    def discover(self, ctx: Context) -> Iterator[Candidate]:
        roots = [Path.home() / "code", Path.home() / "projects", Path.home() / "dev", Path.home()]
        roots = [r for r in roots if r.exists()]
        min_idle_days = ctx.config.get("plugin_settings", {}).get("node.modules", {}).get("min_idle_days", 180)
        large_threshold = 100 * 1024 * 1024
        cutoff = datetime.now() - timedelta(days=min_idle_days)
        seen: set[Path] = set()

        for root in roots:
            for nm_path in _find_node_modules(root, seen):
                package_json = nm_path.parent / "package.json"
                size = _du(nm_path)

                if not package_json.exists():
                    yield Candidate(
                        id=f"node:{nm_path}",
                        category="node.modules",
                        size_bytes=size,
                        path_or_handle=str(nm_path),
                        confidence=Confidence.SAFE,
                        reason="node_modules in directory with no package.json.",
                        metadata={"path": str(nm_path), "project": str(nm_path.parent)},
                    )
                    continue

                mtime = datetime.fromtimestamp(package_json.stat().st_mtime)
                if mtime < cutoff:
                    # Also check if any node process is using this nm_path
                    in_use = _is_in_use_by_node(nm_path)
                    if not in_use:
                        yield Candidate(
                            id=f"node:{nm_path}",
                            category="node.modules",
                            size_bytes=size,
                            path_or_handle=str(nm_path),
                            confidence=Confidence.SAFE,
                            reason=(
                                f"package.json last modified {min_idle_days}+ days ago "
                                "and not in use by a running Node process."
                            ),
                            metadata={"path": str(nm_path), "project": str(nm_path.parent), "mtime": mtime.isoformat()},
                        )
                    else:
                        yield Candidate(
                            id=f"node:{nm_path}",
                            category="node.modules",
                            size_bytes=size,
                            path_or_handle=str(nm_path),
                            confidence=Confidence.MANUAL,
                            reason="package.json is old but a running Node process appears to be using this directory.",
                            metadata={"path": str(nm_path), "project": str(nm_path.parent)},
                        )
                else:
                    if size > large_threshold:
                        yield Candidate(
                            id=f"node:{nm_path}",
                            category="node.modules",
                            size_bytes=size,
                            path_or_handle=str(nm_path),
                            confidence=Confidence.REVIEW,
                            reason=(
                                f"Large node_modules ({self._human_size(size)}) "
                                "in an active project. Review before removal."
                            ),
                            metadata={"path": str(nm_path), "project": str(nm_path.parent)},
                        )
                    else:
                        yield Candidate(
                            id=f"node:{nm_path}",
                            category="node.modules",
                            size_bytes=size,
                            path_or_handle=str(nm_path),
                            confidence=Confidence.MANUAL,
                            reason="Project appears active. node_modules will reinstall with `npm install`.",
                            metadata={"path": str(nm_path), "project": str(nm_path.parent)},
                        )

    def remove(self, candidate: Candidate, ctx: Context) -> RemovalResult:
        path = Path(candidate.path_or_handle)
        if not path.exists():
            return RemovalResult(success=False, bytes_freed=0, log="Path does not exist")
        try:
            size = _du(path)
            import shutil

            shutil.rmtree(path)
            return RemovalResult(success=True, bytes_freed=size, log="Deleted")
        except Exception as exc:
            return RemovalResult(success=False, bytes_freed=0, log=str(exc))

    def supports_quarantine(self) -> bool:
        return True

    def quarantine(self, candidate: Candidate, ctx: Context, quarantine_dir: str, run_id: str | None = None) -> RemovalResult:
        from bekas.quarantine import move_to_quarantine

        path = Path(candidate.path_or_handle)
        size = _du(path)
        try:
            dest = move_to_quarantine(run_id or "quarantine", path, candidate.category, size, candidate.metadata)
            return RemovalResult(success=True, bytes_freed=size, undo_token=str(dest), log="quarantined")
        except Exception as exc:
            return RemovalResult(success=False, bytes_freed=0, log=str(exc))

    @staticmethod
    def _human_size(size_bytes: int) -> str:
        from bekas.formatters import _human_size as fmt_human_size

        return fmt_human_size(size_bytes)


def _find_node_modules(root: Path, seen: set[Path]) -> Iterator[Path]:
    for dirpath, dirnames, _ in os.walk(root):
        dp = Path(dirpath)
        for d in list(dirnames):
            if d == "node_modules":
                nm = dp / d
                try:
                    real = nm.resolve()
                except OSError:
                    real = nm
                if real not in seen:
                    seen.add(real)
                    yield real
                dirnames.remove(d)


def _is_in_use_by_node(path: Path) -> bool:
    """Best-effort check if a node process has this path open."""
    try:
        import psutil

        for proc in psutil.process_iter(["name", "open_files"]):
            if proc.info["name"] in ("node", "node.exe"):
                try:
                    for f in proc.open_files() or []:
                        if str(path) in f.path:
                            return True
                except (psutil.AccessDenied, psutil.NoSuchProcess):
                    pass
    except Exception:
        pass
    return False


def _du(path: Path) -> int:
    total = 0
    try:
        if path.is_file():
            return path.stat().st_size
        for root, _, files in os.walk(path):
            for f in files:
                try:
                    total += os.path.getsize(os.path.join(root, f))
                except OSError:
                    pass
    except OSError:
        pass
    return total
