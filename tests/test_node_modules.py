"""Tests for node_modules plugin."""

from pathlib import Path

from bekas.models import Context, RemovalResult
from bekas.plugins.node_modules import NodeModulesPlugin, _du, _find_node_modules, _is_in_use_by_node


def test_find_node_modules(tmp_path):
    nm = tmp_path / "project" / "node_modules"
    nm.mkdir(parents=True)
    seen: set[Path] = set()
    found = list(_find_node_modules(tmp_path, seen))
    assert any(nm.resolve() == f for f in found)


def test_is_in_use_by_node_returns_bool():
    # Best-effort, mostly returns False in test environment
    assert isinstance(_is_in_use_by_node(Path("/tmp/nm")), bool)


def test_discover_and_remove(tmp_path, monkeypatch):
    home = tmp_path
    monkeypatch.setattr(Path, "home", lambda: home)
    code = home / "code"
    code.mkdir()

    # orphaned node_modules (no package.json)
    orphan = code / "orphan" / "node_modules"
    orphan.mkdir(parents=True)
    (orphan / "foo").mkdir()
    (orphan / "foo" / "package.json").write_text("{}")

    # active project with package.json
    active = code / "active" / "node_modules"
    active.mkdir(parents=True)
    (active / "bar").mkdir()
    pkg = code / "active" / "package.json"
    pkg.write_text("{}")

    p = NodeModulesPlugin()
    ctx = Context(dry_run=True, config={})
    candidates = list(p.discover(ctx))
    ids = [c.id for c in candidates]
    assert any("orphan" in i for i in ids)
    assert any("active" in i for i in ids)

    # Remove
    c = next(c for c in candidates if "orphan" in c.id)
    result = p.remove(c, ctx)
    assert isinstance(result, RemovalResult)


def test_du(tmp_path):
    d = tmp_path / "a"
    d.mkdir()
    (d / "f1").write_text("hello")
    (d / "f2").write_text("world")
    assert _du(d) == 10


def test_supports():
    p = NodeModulesPlugin()
    assert p.supports_quarantine() is True
