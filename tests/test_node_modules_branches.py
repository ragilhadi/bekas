"""Branch coverage tests for node_modules plugin edge cases."""

from __future__ import annotations

import os
from datetime import datetime, timedelta
from pathlib import Path
from unittest.mock import patch

from bekas.models import Candidate, Confidence, Context
from bekas.plugins.node_modules import NodeModulesPlugin, _du, _find_node_modules, _is_in_use_by_node


def _make_ctx(**kwargs):
    return Context(dry_run=True, config=kwargs)


class TestNodeModulesDiscover:
    def test_no_valid_roots(self, tmp_path):
        """If no roots exist, yield nothing."""
        home = tmp_path / "home"
        home.mkdir()
        with patch.object(Path, "home", return_value=home):
            plugin = NodeModulesPlugin()
            cands = list(plugin.discover(_make_ctx()))
            assert cands == []

    def test_no_package_json(self, tmp_path):
        """node_modules without package.json → SAFE."""
        home = tmp_path / "home"
        home.mkdir()
        proj = home / "code" / "oldproj"
        nm = proj / "node_modules"
        nm.mkdir(parents=True)
        (nm / "foo").write_text("x")
        with patch.object(Path, "home", return_value=home):
            plugin = NodeModulesPlugin()
            cands = list(plugin.discover(_make_ctx()))
            assert len(cands) == 1
            assert cands[0].confidence == Confidence.SAFE
            assert "no package.json" in cands[0].reason

    def test_old_package_json_in_use(self, tmp_path):
        """Old package.json but node process in use → MANUAL."""
        home = tmp_path / "home"
        home.mkdir()
        proj = home / "code" / "oldproj"
        nm = proj / "node_modules"
        nm.mkdir(parents=True)
        (nm / "foo").write_text("x")
        pkg = proj / "package.json"
        pkg.write_text('{"name":"old"}')
        old = datetime.now() - timedelta(days=200)
        os.utime(pkg, (old.timestamp(), old.timestamp()))
        with (
            patch.object(Path, "home", return_value=home),
            patch("bekas.plugins.node_modules._is_in_use_by_node", return_value=True),
        ):
            plugin = NodeModulesPlugin()
            cands = list(plugin.discover(_make_ctx()))
            assert len(cands) == 1
            assert cands[0].confidence == Confidence.MANUAL
            assert "running Node" in cands[0].reason

    def test_active_project_large_nm(self, tmp_path):
        """Active project with large node_modules → REVIEW."""
        home = tmp_path / "home"
        home.mkdir()
        proj = home / "code" / "activeproj"
        nm = proj / "node_modules"
        nm.mkdir(parents=True)
        # Write a big file to exceed 100MB threshold
        big = nm / "big.bin"
        big.write_text("x" * (110 * 1024 * 1024))
        pkg = proj / "package.json"
        pkg.write_text('{"name":"active"}')
        with patch.object(Path, "home", return_value=home):
            plugin = NodeModulesPlugin()
            cands = list(plugin.discover(_make_ctx()))
            assert len(cands) == 1
            assert cands[0].confidence == Confidence.REVIEW
            assert "Large" in cands[0].reason

    def test_active_project_small_nm(self, tmp_path):
        """Active project with small node_modules → MANUAL."""
        home = tmp_path / "home"
        home.mkdir()
        proj = home / "code" / "activeproj"
        nm = proj / "node_modules"
        nm.mkdir(parents=True)
        (nm / "foo").write_text("x")
        pkg = proj / "package.json"
        pkg.write_text('{"name":"active"}')
        with patch.object(Path, "home", return_value=home):
            plugin = NodeModulesPlugin()
            cands = list(plugin.discover(_make_ctx()))
            assert len(cands) == 1
            assert cands[0].confidence == Confidence.MANUAL


class TestNodeModulesHelpers:
    def test_find_node_modules_oserror(self, tmp_path):
        """_find_node_modules handles OSError on resolve."""
        root = tmp_path / "root"
        root.mkdir()
        nm = root / "node_modules"
        nm.mkdir()
        with patch("pathlib.Path.resolve", side_effect=OSError("boom")):
            cands = list(_find_node_modules(root, set()))
            # OSError means real = nm (fallback), so it still yields
            assert len(cands) == 1

    def test_is_in_use_by_node_no_psutil(self):
        """If psutil is unavailable, return False."""
        with patch.dict("sys.modules", {"psutil": None}):
            assert _is_in_use_by_node(Path("/tmp/fake")) is False

    def test_is_in_use_by_node_access_denied(self):
        """psutil AccessDenied is skipped."""
        import psutil

        class FakeProc:
            def __init__(self):
                self.info = {"name": "node"}

            def open_files(self):
                raise psutil.AccessDenied

        with patch("psutil.process_iter", return_value=[FakeProc()]):
            assert _is_in_use_by_node(Path("/tmp/fake")) is False

    def test_du_file(self, tmp_path):
        """_du on a file returns its size."""
        f = tmp_path / "file.txt"
        f.write_text("hello")
        assert _du(f) == 5

    def test_du_oserror(self, tmp_path):
        """_du on missing path returns 0."""
        assert _du(tmp_path / "missing") == 0


class TestNodeModulesRemove:
    def test_remove_path_gone(self, tmp_path):
        """remove() when path is gone."""
        plugin = NodeModulesPlugin()
        c = Candidate(
            id="node:x",
            category="node.modules",
            size_bytes=1,
            path_or_handle=str(tmp_path / "gone"),
            confidence=Confidence.SAFE,
            reason="r",
        )
        result = plugin.remove(c, _make_ctx())
        assert result.success is False

    def test_remove_rmtree_exception(self, tmp_path):
        """remove() catches shutil.rmtree exceptions."""
        import shutil

        plugin = NodeModulesPlugin()
        d = tmp_path / "node_modules"
        d.mkdir()
        (d / "file.txt").write_text("x")
        with patch.object(shutil, "rmtree", side_effect=PermissionError("denied")):
            c = Candidate(
                id="node:x",
                category="node.modules",
                size_bytes=1,
                path_or_handle=str(d),
                confidence=Confidence.SAFE,
                reason="r",
            )
            result = plugin.remove(c, _make_ctx())
            assert result.success is False
            assert "denied" in result.log

    def test_quarantine_exception(self, tmp_path):
        """quarantine() catches exceptions."""
        plugin = NodeModulesPlugin()
        d = tmp_path / "node_modules"
        d.mkdir()
        (d / "file.txt").write_text("x")
        with patch("bekas.quarantine.move_to_quarantine", side_effect=RuntimeError("boom")):
            c = Candidate(
                id="node:x",
                category="node.modules",
                size_bytes=1,
                path_or_handle=str(d),
                confidence=Confidence.SAFE,
                reason="r",
            )
            result = plugin.quarantine(c, _make_ctx(), str(tmp_path))
            assert result.success is False
